int	ft_count_if(char **tab, int length, int(*f)(char*))
{
	int	i;
	int	temp;

	temp = 0;
	i = 0;
	while (i < length)
	{
		if (f(tab[i]) != 0)
			temp++;
		i++;
	}
	return (temp);
}
